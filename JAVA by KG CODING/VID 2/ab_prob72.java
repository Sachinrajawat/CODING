//calculate the area and circumference of a circle for a given radius using MATH.PI
public class ab_prob72 {
    public static void main(String[] args) {
    int radius=3;
    int area=(int)Math.PI*radius*radius;
    int circumference=(int)Math.PI*2*radius;
    System.out.println("Area of a circle is "+area+"\nCircumference of a circle is "+circumference);
    }
}
